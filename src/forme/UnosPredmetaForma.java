    private void popuniComboBox() {

        List<Katedra> lista = Controller.getInstance().vratiListuKatedri();
        lista.add(0, null);
        for (Katedra k : lista) {
            jComboBoxKatedra.addItem(k);
        }

    }

    private void pripremiFormu() {
        jTextFieldESBP.setText("");
        jTextFieldNaziv.setText("");
        jComboBoxKatedra.setSelectedItem(null);
    }

    private void popuniFormu(Predmet p) {
        
        jTextFieldNaziv.setText(p.getNaziv());
        jTextFieldESBP.setText(p.getEspb()+"");
        jComboBoxKatedra.setSelectedItem(p.getKatedra());
        
        jTextFieldNaziv.setEnabled(false);
        jTextFieldESBP.setEnabled(false);
        jComboBoxKatedra.setEnabled(false);
    
    }
    private Predmet validiran(){
        if (jTextFieldNaziv == null) {
            JOptionPane.showMessageDialog(this, "Polje naziv je prazno!");
            return null;
        }
        String naziv = jTextFieldNaziv.getText();
        if (naziv == null || naziv.isEmpty() || !naziv.matches("[a-zA-Z]+") || naziv.length() > 100) {
            JOptionPane.showMessageDialog(this, "Pogresan naziv!");
            return null;
        }

        if (jTextFieldESBP == null || jTextFieldESBP.getText().matches("[a-zA-Z]+")) {
            JOptionPane.showMessageDialog(this, "Polje esbp je prazno ili sadrzi slova!");
            return null;
        }

        int esbp = Integer.parseInt(jTextFieldESBP.getText());
        if(esbp<2 || esbp>10){
            JOptionPane.showMessageDialog(this, "Broj esbp nije odgovarajuci (2-10)");
            return null;
        }
        
        if(jComboBoxKatedra.getSelectedItem()==null){
            JOptionPane.showMessageDialog(this, "Nije odabrana katedra!");
            return null;
        }
        
        Katedra k = (Katedra) jComboBoxKatedra.getSelectedItem();
        
        
        
        
        Predmet p = new Predmet(0, naziv, esbp, k);
        return p;
    }
