    private void jButtonSacuvajIzmenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajIzmenuActionPerformed
        
        
        Predmet validiran=validiran();
        Predmet promenjen=new Predmet(odabran.getId(), validiran.getNaziv(), validiran.getEspb(), validiran.getKatedra());
        int odg=JOptionPane.showConfirmDialog(this, "Da li zelite da sacuvate izmene", "Provera", JOptionPane.YES_NO_OPTION);
        if(odg==JOptionPane.YES_OPTION){
           boolean uspesno=Controller.getInstance().izmeniPredmet(promenjen);
           if(uspesno){
               JOptionPane.showMessageDialog(this, "Promene su sacuvane u bazi");
               this.dispose();
               PrikazPredmetaForma ppf=new PrikazPredmetaForma();
               ppf.osveziTabelu();
               
           }
           else{
               JOptionPane.showMessageDialog(this, "Greska pri cuvanju promena");
           }
        }
        
        
        
    }//GEN-LAST:event_jButtonSacuvajIzmenuActionPerformed

