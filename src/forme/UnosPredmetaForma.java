    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed

        
        
        
        Predmet p = validiran();

        boolean uspesno = Controller.getInstance().ubaciPredmet(p);
        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Uspesno unet predmet u bazu");
            int odg=JOptionPane.showConfirmDialog(this, "Da li zelite da nastavnite sa dodavanjem?", "Potvrda", JOptionPane.YES_NO_OPTION);
            if(odg==JOptionPane.YES_OPTION){
                pripremiFormu();
            }else{
                this.dispose();
            }
        
        
        } else {
            JOptionPane.showMessageDialog(this, "Greska pri unosu predmeta u bazu");
        }


    }//GEN-LAST:event_jButtonSacuvajActionPerformed

